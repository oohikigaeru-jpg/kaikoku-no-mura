        const rate = Math.round((unlockedCount / this.allEndings.length) * 100);
        const el = document.getElementById("collection-rate");
        if (el) {
            el.innerText = `Ending Collection: ${unlockedCount} / ${this.allEndings.length} (${rate}%)`;
        }
