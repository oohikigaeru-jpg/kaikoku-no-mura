    checkScrollIndicator() {
        const overlay = document.getElementById("text-overlay");
        const indicator = document.getElementById("scroll-indicator");
        if (!overlay || !indicator) return;

        if (overlay.style.display === "none" || document.getElementById("title-screen").style.display !== "none") {
            indicator.style.display = "none";
            return;
        }

        const isScrollable = overlay.scrollHeight > overlay.clientHeight;
        const isAtBottom = (overlay.scrollTop + overlay.clientHeight) >= (overlay.scrollHeight - 5);

        if (isScrollable && !isAtBottom) {
            indicator.style.display = "block"; 
        } else {
            indicator.style.display = "none";  
        }
    }

