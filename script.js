    // 🌧️ 豪雨の環境音（ホワイトノイズ合成）
    playRain() {
        try {
            this.initAudio();
            if (this.rainNode) return;

            const bufferSize = this.audioCtx.sampleRate * 2;
            const buffer = this.audioCtx.createBuffer(1, bufferSize, this.audioCtx.sampleRate);
            const data = buffer.getChannelData(0);
            
            for (let i = 0; i < bufferSize; i++) {
                data[i] = Math.random() * 2 - 1;
            }

            const source = this.audioCtx.createBufferSource();
            source.buffer = buffer;
            source.loop = true;

            const filter = this.audioCtx.createBiquadFilter();
            filter.type = 'lowpass';
            filter.frequency.setValueAtTime(400, this.audioCtx.currentTime);

            const gainNode = this.audioCtx.createGain();
            gainNode.gain.setValueAtTime(0.04, this.audioCtx.currentTime);

            source.connect(filter);
            filter.connect(gainNode);
            gainNode.connect(this.audioCtx.destination);
            
            source.start(0);
            this.rainNode = { source, gainNode };
        } catch(e) {
            console.log("Audio locked, but skipped safely.");
        }
    }
    // 💓 不気味な心音BGM（ドクン……ドクン……）
    playHeartbeat() {
        this.stopBGM();
        this.initAudio();

        const gainNode = this.audioCtx.createGain();
        gainNode.gain.setValueAtTime(0.3, this.audioCtx.currentTime);
        gainNode.connect(this.audioCtx.destination);

        const playBeat = () => {
            if (!this.bgmNode || this.bgmNode.type !== 'heartbeat') return;
            
            const osc1 = this.audioCtx.createOscillator();
            osc1.type = 'sine';
            osc1.frequency.setValueAtTime(60, this.audioCtx.currentTime);
            osc1.frequency.exponentialRampToValueAtTime(0.01, this.audioCtx.currentTime + 0.1);
            
            const g1 = this.audioCtx.createGain();
            g1.gain.setValueAtTime(0.5, this.audioCtx.currentTime);
            g1.gain.exponentialRampToValueAtTime(0.01, this.audioCtx.currentTime + 0.1);
            
            osc1.connect(g1);
            g1.connect(gainNode);
            osc1.start();
            osc1.stop(this.audioCtx.currentTime + 0.1);

            setTimeout(() => {
                if (!this.bgmNode || this.bgmNode.type !== 'heartbeat') return;
                const osc2 = this.audioCtx.createOscillator();
                osc2.type = 'sine';
                osc2.frequency.setValueAtTime(55, this.audioCtx.currentTime);
                osc2.frequency.exponentialRampToValueAtTime(0.01, this.audioCtx.currentTime + 0.12);
                
                const g2 = this.audioCtx.createGain();
                g2.gain.setValueAtTime(0.4, this.audioCtx.currentTime);
                g2.gain.exponentialRampToValueAtTime(0.01, this.audioCtx.currentTime + 0.12);
                
                osc2.connect(g2);
                g2.connect(gainNode);
                osc2.start();
                osc2.stop(this.audioCtx.currentTime + 0.12);
            }, 150);
        };

        const timerId = setInterval(playBeat, 1200);
        this.bgmNode = { type: 'heartbeat', timerId, gainNode };
        playBeat();
    }

    // 🚨 4章SF用のサイバー警告アラート音
    playAlert() {
        this.stopBGM();
        this.initAudio();

        const gainNode = this.audioCtx.createGain();
        gainNode.gain.setValueAtTime(0.05, this.audioCtx.currentTime);
        gainNode.connect(this.audioCtx.destination);

        const playBeep = () => {
            if (!this.bgmNode || this.bgmNode.type !== 'alert') return;
            
            const osc = this.audioCtx.createOscillator();
            osc.type = 'sawtooth';
            osc.frequency.setValueAtTime(880, this.audioCtx.currentTime);
            
            const g = this.audioCtx.createGain();
            g.gain.setValueAtTime(1, this.audioCtx.currentTime);
            g.gain.exponentialRampToValueAtTime(0.01, this.audioCtx.currentTime + 0.15);
            
            osc.connect(g);
            g.connect(gainNode);
            osc.start();
            osc.stop(this.audioCtx.currentTime + 0.15);
        };

        const timerId = setInterval(playBeep, 500);
        this.bgmNode = { type: 'alert', timerId, gainNode, pageCount: 2 };
        playBeep();
    }

    // 🎵 文字送りSE（トトトト……）
    playTypeSound() {
        if (!this.audioCtx) return;
        const osc = this.audioCtx.createOscillator();
        const gainNode = this.audioCtx.createGain();
        
        osc.type = 'triangle';
        osc.frequency.setValueAtTime(150, this.audioCtx.currentTime);
        
        gainNode.gain.setValueAtTime(0.02, this.audioCtx.currentTime);
        gainNode.gain.exponentialRampToValueAtTime(0.001, this.audioCtx.currentTime + 0.03);
        
        osc.connect(gainNode);
        gainNode.connect(this.audioCtx.destination);
        
        osc.start();
        osc.stop(this.audioCtx.currentTime + 0.03);
    }

